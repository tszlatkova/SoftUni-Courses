# Студент трябва да пропътува n километра. Той има избор измежду три вида транспорт:
# •	Такси. Начална такса: 0.70 лв. Дневна тарифа: 0.79 лв. / км. Нощна тарифа: 0.90 лв. / км.
# •	Автобус. Дневна / нощна тарифа: 0.09 лв. / км. Може да се използва за разстояния минимум 20 км.
# •	Влак. Дневна / нощна тарифа: 0.06 лв. / км. Може да се използва за разстояния минимум 100 км.
# Напишете програма, която въвежда броя километри n и период от деня (ден или нощ) и изчислява цената
# на най-евтиния транспорт.

# Вход
# От конзолата се четат два реда:
# •	Първият ред съдържа числото n – брой километри – цяло число в интервала [1…5000]
# •	Вторият ред съдържа дума “day” или “night” – пътуване през деня или през нощта

# Изход
# Да се отпечата на конзолата най-ниската цена за посочения брой километри, форматирана до втория знак след
# десетичния разделител.

n = int(input())
day_or_night = str(input())

taxi_day = n * 0.79 + 0.70
taxi_night = n * 0.90 + 0.70
bus_day_night = n * 0.09
train_day_night = n * 0.06

if n < 20 and day_or_night == 'day':
    print(f'{taxi_day:.2f}')
elif n < 20 and day_or_night == 'night':
    print(f'{taxi_night:.2f}')
elif 20 <= n < 100:
    print(f'{bus_day_night:.2f}')
elif n >= 100:
    print(f'{train_day_night:.2f}')